				{
					$('.left_door').removeClass('left_door_anim');
					$('.right_door').removeClass('right_door_anim');
					$('.menu_toggle span').text('EXIT').fadeIn(800);
					$('.menu_toggle img').attr('src', 'images/skull_exit.png');
