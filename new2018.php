						$('.moving_witch').css('transform', 'rotateY(360deg)');
					}

					mob_scroll_posn =mob_scroll_posn_new;
				}
				else if(mob_scroll_posn_new < mob_scroll_posn )
				{
					$('.moving_witch').css('left', '35%');

					$('.moving_witch').css('transform', 'rotateY(180deg)');

